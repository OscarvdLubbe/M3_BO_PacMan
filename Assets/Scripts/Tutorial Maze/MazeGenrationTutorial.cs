    }

    void GenerateMaze()
    {
        Vector2 tileSize = wall.GetComponent<Renderer>().bounds.size;

        float ox = (mapMaker.GetLength(1) * tileSize.x - 1) / 2;
        float oy = (mapMaker.GetLength(0) * tileSize.y - 1) / 2;

        for (int y = 0; y < mapMaker.GetLength(0); y++)
        {
            for (int x = 0; x < mapMaker.GetLength(1); x++)
            {
                float ux = (x * tileSize.x) - ox;
                float uy = (-y * tileSize.y) + oy;

                switch (mapMaker[y, x])
                {
                    case 0:
                        GameObject cube = Instantiate(wall, new Vector3(ux, uy, 0), quaternion.identity);
                        cube.transform.SetParent(Map.transform);
                        break;

                    case 1:
                        GameObject air = Instantiate(Path, new Vector3(ux, uy, 0), quaternion.identity);
                        air.transform.SetParent(Map.transform);
                        air.GetComponent<Renderer>().material.color = Color.white;
                        break;
                    case 2:
                        GameObject air2 = Instantiate(FakePath, new Vector3(ux, uy, 0), quaternion.identity);
                        air2.transform.SetParent(Map.transform);
                        air2.GetComponent<Renderer>().material.color = Color.white;
                        break;
                    case 3:
                        air = Instantiate(Path, new Vector3(ux, uy, 0), quaternion.identity);
                        GameObject Waypoint = Instantiate(waypoint, new Vector3(ux, uy, 0), quaternion.identity);
                        Waypoint.transform.SetParent(Map.transform);
                        air.transform.SetParent(Map.transform);
                        break;
                    case 4:
                        air = Instantiate(Path, new Vector3(ux, uy, 0), quaternion.identity);
                        Waypoint = Instantiate(waypoint, new Vector3(ux, uy, 0), quaternion.identity);
                        air.transform.SetParent(Map.transform);
                        Waypoint.transform.SetParent(Map.transform);
                        GameObject Ghost = Instantiate(ghost, new Vector3(ux, uy, -0.25f), quaternion.identity);
                        Ghost.transform.SetParent(Map.transform);
                        break;
                    case 5:
                        air = Instantiate(Path, new Vector3(ux, uy, 0), quaternion.identity);
                        Waypoint = Instantiate(waypoint, new Vector3(ux, uy, 0), quaternion.identity);
                        air.transform.SetParent(Map.transform);
                        Waypoint.transform.SetParent(Map.transform);
                        GameObject Orb = Instantiate(orb, new Vector3(ux, uy, -0.25f), quaternion.identity);
                        Orb.transform.SetParent(Map.transform);
                        break;
                }
            }
        }
    }
