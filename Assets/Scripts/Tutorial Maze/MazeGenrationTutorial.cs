    void GenerateMaze()
    {
        Vector2 tileSize = wall.GetComponent<Renderer>().bounds.size;

        float ox = (mapMaker.GetLength(1) * tileSize.x - 1) / 2;
        float oy = (mapMaker.GetLength(0) * tileSize.y - 1) / 2;

//maak waypoint list 
        for (int y = 0; y < mapMaker.GetLength(0); y++)
        {
            for (int x = 0; x < mapMaker.GetLength(1); x++)
            {
                float ux = (x * tileSize.x) - ox;
                float uy = (-y * tileSize.y) + oy;

                switch (mapMaker[y, x])
                {
                    case 0:
                        Wall cube = Instantiate(wall, new Vector3(ux, uy), quaternion.identity);
                        cube.transform.SetParent(Map.transform);
                        break;

                    case 1:
                        GameObject air = Instantiate(Path, new Vector3(ux, uy), quaternion.identity);
                        air.transform.SetParent(Map.transform);
                        air.GetComponent<Renderer>().material.color = Color.white;
                        break;
                    case 2:
                        GameObject air2 = Instantiate(FakePath, new Vector3(ux, uy), quaternion.identity);
                        air2.transform.SetParent(Map.transform);
                        air2.GetComponent<Renderer>().material.color = Color.white;
                        break;
                    case 3:
                        GameObject Waypoint = Instantiate(TheWayToGo, new Vector3(ux, uy), quaternion.identity);
                        //onthoud de x en y (van je map)
                        Waypoint.transform.SetParent(Map.transform);
                        //Waypoint.GetComponent<Renderer>().material.color = Color.white;
                        break;
                }
            }
        }
    for (int y = 0; y < mapMaker.GetLength(0); y++)
        {
            for (int x = 0; x < mapMaker.GetLength(1); x++)
            {
                if (mapMaker[y, x] == 3)
                {
                    Debug.Log($"Youre x is :{x} and youre y is :{y}");
                }
            //loop door elke waypoint en kijk naar boven/links etc voor de eerste muur (0) of waypoint(3)
            //koppel de N Z W E waypoints die vindt
            }
        }
    }
